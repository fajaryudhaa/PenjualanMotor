    protected void datatable(){
        Object[] Baris = {"Kode", "Merek", "Nama","Jenis", "Warna","Harga"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txtcari.getText();
        tabel.setModel(tabmode);
        try {
        String sql = "SELECT * FROM datamotor where kode like '%"+cariitem+"%' or nama like '%"+cariitem+"%' order by kode asc";
        
            //java.sql.Statement stat = conn.createStatement();
            RsBook = KoneksiDB.getRS(sql);
            while(RsBook.next()){
                String a = RsBook.getString("kode");
                String b = RsBook.getString("Merek");
                String c = RsBook.getString("Nama");
                String d = RsBook.getString("Jenis");
                String e = RsBook.getString("Warna");
                String f = RsBook.getString("Harga");
                
                
                String[] data = {a, b, c, d, e, f};
                tabmode.addRow(data);
            }
        }catch (SQLException e){
        }
    }

private void autonumber(){
        try
        {
            String sql="select * from datamotor order by kode desc";
            RsBook = KoneksiDB.getRS(sql); 
            if (RsBook.next()) {
                String nolok = RsBook.getString("kode").substring(4);
                String AN = ""+ (Integer.parseInt(nolok) + 1);
                String Nol = "";
                if (AN.length()==1)
                {Nol="000";}
                else if(AN.length()==2)
                {Nol = "00";}
                else if (AN.length()==3)
                {Nol="0";}
                else if (AN.length()==4)
                {Nol="";}
                txtkode.setText("MTR-"+Nol+AN);
            }
            else {
                txtkode.setText("MTR-0001");
            }
                        
        }catch(Exception ex){
                System.out.println(ex);
        }
    }

